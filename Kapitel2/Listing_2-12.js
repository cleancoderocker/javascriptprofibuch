const operations = {
  add: function(x, y) {
    return x + y;
  },
  subtract: function(x, y) {
    return x - y;
  },
  multiply: function(x, y) {
    return x * y;
  },
  divide: function(x, y) {
    return x / y;
  }
};
console.log(operations.add(2, 2));
console.log(operations.subtract(2, 2));
console.log(operations.multiply(2, 2));
console.log(operations.divide(2, 2));
